// Orden de las operaciones
let resultado;

resultado = (20 + 30) * 2;

resultado = (100  + 200 + 300) * 1.16;

console.log(resultado);

// Incrementos
let puntaje = 10;

// Incrementa en 1 ++
puntaje++; // 10
++puntaje; // 12

// Decremento en 1 --
puntaje--;
--puntaje;

puntaje += 10;

console.log(puntaje);